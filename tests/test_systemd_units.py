"""
Systemd unit linter — catches known footguns in generated service/path files.

Prevents:
- StartLimitBurst in [Service] (silently ignored, must be in [Unit])
- Restart=always (infinite crash loops, must be on-failure)
- Missing CI=true (causes @clack/prompts stdin block in non-TTY context)
- Missing StartLimitBurst (no crash loop protection)
- ExecStartPost scripts using 'local' outside functions
"""

import os
import re

import pytest

REPO_DIR = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
SCRIPTS_DIR = os.path.join(REPO_DIR, "scripts")


def read_generator_script(name):
    """Read a generator script and return its content."""
    path = os.path.join(SCRIPTS_DIR, name)
    if not os.path.exists(path):
        return None
    with open(path) as f:
        return f.read()


def parse_unit_sections(unit_text):
    """Parse a systemd unit file into sections."""
    sections = {}
    current_section = None
    for line in unit_text.strip().splitlines():
        line = line.strip()
        if line.startswith("[") and line.endswith("]"):
            current_section = line[1:-1]
            sections[current_section] = {}
        elif "=" in line and current_section:
            key, _, value = line.partition("=")
            key = key.strip()
            # Accumulate multiple values for same key (e.g., Environment)
            if key in sections[current_section]:
                if isinstance(sections[current_section][key], list):
                    sections[current_section][key].append(value.strip())
                else:
                    sections[current_section][key] = [
                        sections[current_section][key], value.strip()
                    ]
            else:
                sections[current_section][key] = value.strip()
    return sections


class TestSessionUnits:
    """Lint session service generator for systemd footguns."""

    @pytest.fixture(autouse=True)
    def setup(self):
        self.content = read_generator_script("generate-session-services.sh")
        if not self.content:
            pytest.skip("generate-session-services.sh not found")

    def test_start_limit_burst_not_in_service_section(self):
        """StartLimitBurst must not appear after [Service] header."""
        in_service = False
        for line in self.content.splitlines():
            if "[Service]" in line:
                in_service = True
            elif line.strip().startswith("[") and line.strip().endswith("]"):
                in_service = False
            if in_service and "StartLimitBurst" in line:
                pytest.fail("StartLimitBurst in [Service] section — must be in [Unit]")

    def test_start_limit_burst_in_unit_section(self):
        """StartLimitBurst must appear after [Unit] header."""
        in_unit = False
        for line in self.content.splitlines():
            if "[Unit]" in line:
                in_unit = True
            elif line.strip().startswith("[") and line.strip().endswith("]"):
                in_unit = False
            if in_unit and "StartLimitBurst" in line:
                return  # Found it in [Unit] — pass
        pytest.fail("StartLimitBurst not found in [Unit] section")

    def test_restart_on_failure(self):
        """Restart must be on-failure, never always."""
        assert "Restart=on-failure" in self.content
        assert "Restart=always" not in self.content

    def test_ci_env_set(self):
        """CI=true prevents @clack/prompts stdin block."""
        assert "CI=true" in self.content


