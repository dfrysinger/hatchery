class TestSafeguardPathUnits:
    """Validate .path units clean up watched files."""

    def test_safe_mode_handler_removes_unhealthy_marker(self):
        """safe-mode-handler.sh must remove the unhealthy marker on ALL exit paths."""
        handler = os.path.join(SCRIPTS_DIR, "safe-mode-handler.sh")
        if not os.path.exists(handler):
            pytest.skip("safe-mode-handler.sh not found")

        with open(handler) as f:
            content = f.read()

        # Must have a trap that runs cleanup
        assert re.search(r'trap\b.*cleanup|trap\b.*EXIT', content), \
            "safe-mode-handler.sh missing EXIT trap for cleanup"

        # Must remove unhealthy marker
        assert "rm -f" in content and "HC_UNHEALTHY_MARKER" in content, \
            "safe-mode-handler.sh must remove HC_UNHEALTHY_MARKER to prevent .path re-trigger"

    def test_handler_stops_service_on_terminal_failure(self):
        """Terminal failure paths must stop the service."""
        handler = os.path.join(SCRIPTS_DIR, "safe-mode-handler.sh")
        if not os.path.exists(handler):
            pytest.skip("safe-mode-handler.sh not found")

        with open(handler) as f:
            content = f.read()

        # Must call hc_stop_service on exhausted-attempts path
        assert content.count("hc_stop_service") >= 2, \
            "safe-mode-handler.sh must call hc_stop_service on ALL terminal failure paths"


class TestHealthCheckScripts:
    """Validate health check scripts don't have bash footguns."""

    def test_no_local_outside_functions(self):
        """'local' keyword must only appear inside functions."""
        for script_name in ["gateway-health-check.sh", "gateway-e2e-check.sh"]:
            script_path = os.path.join(SCRIPTS_DIR, script_name)
            if not os.path.exists(script_path):
                continue

            with open(script_path) as f:
                lines = f.readlines()

            in_function = False
            brace_depth = 0
            for i, line in enumerate(lines, 1):
                stripped = line.strip()

                # Track function boundaries (simplified)
                if re.match(r'^[a-zA-Z_]\w*\s*\(\)', stripped):
                    in_function = True
                    brace_depth = 0
                if in_function:
                    brace_depth += stripped.count("{") - stripped.count("}")
                    if brace_depth <= 0 and in_function and "{" not in stripped:
                        in_function = False

                if stripped.startswith("local ") and not in_function:
                    pytest.fail(
                        f"{script_name}:{i}: 'local' outside function — "
                        f"fatal error under set -euo pipefail"
                    )


class TestDockerCompose:
    """Validate docker-compose security settings."""

    def test_compose_restart_has_retry_cap(self):
        """Docker restart policy must have a retry cap."""
        generator = os.path.join(SCRIPTS_DIR, "generate-docker-compose.sh")
        if not os.path.exists(generator):
            pytest.skip("generate-docker-compose.sh not found")

        with open(generator) as f:
            content = f.read()

        # Should use on-failure with a cap, not unlimited
