# hc_curl_gateway
# =========================================================================

class TestHcCurlGateway:
    def test_session_mode_direct_curl(self):
        r = run_hc('hc_curl_gateway "council" "/api/health"', env={
            'ISOLATION': 'session', 'GROUP_PORT': '18790'
        })
        assert 'CURL -sf http://127.0.0.1:18790/api/health' in r.stdout

    def test_container_host_network_direct_curl(self):
        r = run_hc('hc_curl_gateway "sandbox" "/"', env={
            'ISOLATION': 'container', 'NETWORK_MODE': 'host', 'GROUP_PORT': '18791'
        })
        assert 'CURL -sf http://127.0.0.1:18791/' in r.stdout

    def test_container_isolated_network_uses_docker_exec(self):
        r = run_hc('hc_curl_gateway "sandbox" "/api/health"', env={
            'ISOLATION': 'container', 'NETWORK_MODE': 'isolated', 'GROUP_PORT': '18791'
        })
        assert 'DOCKER exec openclaw-sandbox' in r.stdout
        assert '18791' in r.stdout

    def test_default_path_is_root(self):
        r = run_hc('hc_curl_gateway "council"', env={
            'ISOLATION': 'session', 'GROUP_PORT': '18790'
        })
        assert '18790/' in r.stdout


